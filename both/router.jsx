/* globals FlowRouter, ReactLayout */

FlowRouter.route('/', {
  action () {
    ReactLayout.render(App.DappsList, {})
  }
});
FlowRouter.route('/dapp/:id', {
  action (params) {
    ReactLayout.render(App.DappsView, {_id: params.id})
  }
});

if (Meteor.isServer) {
  FlowRouter.setDeferScriptLoading(true)
}
